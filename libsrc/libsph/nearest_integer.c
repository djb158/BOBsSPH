#include "nearest_integer.h"

long NearestInteger(double x)
{
  char   fname[]                       = "...NearestInteger";

  double middle            = 0.0;

  middle = 0.5 *(ceil(x) + floor(x));

  if ( x < middle )
  {
    return (long)floor(x);
  }
  else if ( x > middle)
  {
    return (long)ceil(x);
  }
  else
  {
    return (long)ceil(x);
  }
}
